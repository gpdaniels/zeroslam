void save_plot_to_ppm(
    const char* output_filename,
    const int image_width,
    const int image_height,
    const unsigned char* const image_buffer
) {
    std::FILE* const file_pointer = std::fopen(output_filename, "wb");
    if (file_pointer != nullptr) {
        std::fprintf(file_pointer, "P6\n%d %d\n255\n", image_width, image_height);
        const size_t pixel_count = static_cast<size_t>(image_width) * static_cast<size_t>(image_height) * 3;
        std::fwrite(image_buffer, 1, pixel_count, file_pointer);
        std::fclose(file_pointer);
    }
}

void calculate_plot_bounds(
    const std::vector<double>& ground_truth_axis_1_values,
    const std::vector<double>& ground_truth_axis_2_values,
    const std::vector<std::vector<double>>& estimated_trajectories_axis_1_values,
    const std::vector<std::vector<double>>& estimated_trajectories_axis_2_values,
    double& minimum_axis_1,
    double& maximum_axis_1,
    double& minimum_axis_2,
    double& maximum_axis_2
) {
    minimum_axis_1 = *std::min_element(ground_truth_axis_1_values.begin(), ground_truth_axis_1_values.end());
    maximum_axis_1 = *std::max_element(ground_truth_axis_1_values.begin(), ground_truth_axis_1_values.end());
    minimum_axis_2 = *std::min_element(ground_truth_axis_2_values.begin(), ground_truth_axis_2_values.end());
    maximum_axis_2 = *std::max_element(ground_truth_axis_2_values.begin(), ground_truth_axis_2_values.end());

    for (const std::vector<double>& trajectory_values : estimated_trajectories_axis_1_values) {
        for (const double value : trajectory_values) {
            minimum_axis_1 = std::min(minimum_axis_1, value);
            maximum_axis_1 = std::max(maximum_axis_1, value);
        }
    }
    for (const std::vector<double>& trajectory_values : estimated_trajectories_axis_2_values) {
        for (const double value : trajectory_values) {
            minimum_axis_2 = std::min(minimum_axis_2, value);
            maximum_axis_2 = std::max(maximum_axis_2, value);
        }
    }

    const double padding_axis_1 = (maximum_axis_1 - minimum_axis_1) * 0.1 + 0.1;
    const double padding_axis_2 = (maximum_axis_2 - minimum_axis_2) * 0.1 + 0.1;
    
    minimum_axis_1 -= padding_axis_1;
    maximum_axis_1 += padding_axis_1;
    minimum_axis_2 -= padding_axis_2;
    maximum_axis_2 += padding_axis_2;
}

